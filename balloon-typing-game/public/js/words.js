// 랜덤 단어 가져오기
function getRandomWord() {
    // 가중치: 2글자 30%, 3글자 40%, 4글자 20%, 5글자 10%
    const rand = Math.random() * 100;
    let length;

    if (rand < 30) {
        length = 2;
    } else if (rand < 70) {
        length = 3;
    } else if (rand < 90) {
        length = 4;
    } else {
        length = 5;
    }

    const wordList = WORDS[length];
    return wordList[Math.floor(Math.random() * wordList.length)];
}
